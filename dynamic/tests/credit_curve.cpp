void
fd1()
{
    double epsilon = 1e-6;
    std::vector<CDS> cdsInstruments = get_cds();
    const auto yc = get_yc();
    std::vector<double> cdsSpreads(cdsSpreadVals.begin(), cdsSpreadVals.end());

    auto sc = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);

    for (std::size_t j = 0; j < cdsSpreads.size(); ++j) {
        double originalCDSSpread = cdsSpreads[j];
        cdsSpreads[j] = originalCDSSpread + epsilon;
        auto sc_up = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);

        cdsSpreads[j] = originalCDSSpread - epsilon;
        auto sc_dn = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);

        cdsSpreads[j] = originalCDSSpread;

        auto const& sp_up = sc_up.getParams();
        auto const& sp_dn = sc_dn.getParams();
        for (std::size_t i = 1; i < sp_up.size(); ++i) {
            const auto fdSensitivity = (sp_up[i] - sp_dn[i]) / (2.0 * epsilon);
            EXPECT_NEAR_ABS(expectedFirstDerivatives(i - 1, j), fdSensitivity, 2e-7);
        }
    }
}

void
fd2()
{
    double epsilon = 1e-6;

    std::vector<CDS> cdsInstruments = get_cds();
    const auto yc = get_yc();
    std::vector<double> cdsSpreads(cdsSpreadVals.begin(), cdsSpreadVals.end());

    auto sc = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
    auto const& sp = sc.getParams();

    for (std::size_t j = 0; j < cdsSpreads.size(); ++j) {
        for (std::size_t k = 0; k < cdsSpreads.size(); ++k) {
            double originalCDSSpread1 = cdsSpreads[j];
            double originalCDSSpread2 = cdsSpreads[k];

            if (j == k) {
                cdsSpreads[j] = originalCDSSpread1 + epsilon;
                auto sc_up = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_up = sc_up.getParams();

                cdsSpreads[j] = originalCDSSpread1 - epsilon;
                auto sc_dn = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_dn = sc_dn.getParams();

                // f''(x) ≈ (f(x+h) - 2f(x) + f(x-h)) / h^2
                for (std::size_t i = 1; i < sp.size(); ++i) {
                    const auto fdSecondDerivative = (sp_up[i] - 2.0 * sp[i] + sp_dn[i]) / (epsilon * epsilon);

                    const auto expectedSecondDer = expectedSecondDerivativesFD(j, k, i - 1);
                    EXPECT_NEAR_ABS(expectedSecondDer, fdSecondDerivative, 1e-15);
                }
            }
            else {
                cdsSpreads[j] = originalCDSSpread1 + epsilon;
                cdsSpreads[k] = originalCDSSpread2 + epsilon;
                auto sc_up_up = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_up_up = sc_up_up.getParams();

                cdsSpreads[j] = originalCDSSpread1 + epsilon;
                cdsSpreads[k] = originalCDSSpread2 - epsilon;
                auto sc_up_dn = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_up_dn = sc_up_dn.getParams();

                cdsSpreads[j] = originalCDSSpread1 - epsilon;
                cdsSpreads[k] = originalCDSSpread2 + epsilon;
                auto sc_dn_up = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_dn_up = sc_dn_up.getParams();

                cdsSpreads[j] = originalCDSSpread1 - epsilon;
                cdsSpreads[k] = originalCDSSpread2 - epsilon;
                auto sc_dn_dn = createCDSCurve(cdsInstruments, yc, cdsSpreads, pricingDate);
                auto const& sp_dn_dn = sc_dn_dn.getParams();

                for (std::size_t i = 1; i < sp.size(); ++i) {
                    const auto fdSecondDerivative =
                      (sp_up_up[i] - sp_up_dn[i] - sp_dn_up[i] + sp_dn_dn[i]) / (4.0 * epsilon * epsilon);
                    const auto expectedSecondDer = expectedSecondDerivativesFD(j, k, i - 1);
                    EXPECT_NEAR_ABS(expectedSecondDer, fdSecondDerivative, 1e-15);
                }
            }

            cdsSpreads[j] = originalCDSSpread1;
            cdsSpreads[k] = originalCDSSpread2;
        }
    }
}

